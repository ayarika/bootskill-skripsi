    <p> Welcome to BootSkill! Here's a step-by-step guide on how to use this platform effectively, whether you're a student or an organizer.</p>

    <h2> For Students </h2>
    <ul>
        <li>Register and verify your email.</li>
        <li>Login to your account using the credentials.</li>
        <li>Browse available bootcamps from the homepage.</li>
        <li>Click "Enroll" to register in a bootcamp of your choice.</li>
        <li>Upload your payment proof to confirm your registration.</li>
        <li>Check the "Your Course" tab to access your enrolled bootcamps.</li>
    </ul>

    <h2> For Organizer </h2>
    <ul>
        <li>Register as an organizer and fill your profile.</li>
        <li>From the sidebar, go to "Your Event".</li>
        <li>Click "Create Event" to publish a bootcamp.</li>
        <li>After publishing, manage enrollments and attendance.<li>
        <li>Use the dashboard to track transaction and user participation.</li>
    </uL>

